        public static String ConvertColor(int _color)
        {
            switch (_color)
            {
                case P.BLACK:
                    return "BLACK";
                case P.WHITE:
                    return "WHITE";
                default:
                    return "BAD COLOR";
            }
        }

        public static String ConvertOption(int _option)
        {
            switch (_option)
            {
                case P.SELECT:
                    return "Select Piece";
                case P.DROP:
                    return "Drop Piece";
                case P.MOVE_OR_ATTACK:
                    return "Move/Attack";
                case P.PASS:
                    return "Pass Turn";
                case P.DONE:
                    return "Done Setting Up";
                default:
                    return "BAD OPTION";
            }
        }

        public static char ConvertPiece(int _piece_num)
        {
            int b = (_piece_num < 0 ? 32 : 0);
            int c;
            switch (Math.Abs(_piece_num))
            {
                case P.MAR:
                    c = b + 'M';
                    break;
                case P.SPY:
                    c = b + 'Y';
                    break;
                case P.LIE:
                    c = b + 'L';
                    break;
                case P.MAJ:
                    c = b + 'J';
                    break;
                case P.GEN:
                    c = b + 'G';
                    break;
                case P.ARC:
                    c = b + 'A';
                    break;
                case P.KNI:
                    c = b + 'K';
                    break;
                case P.SAM:
                    c = b + 'S';
                    break;
                case P.CAN:
                    c = b + 'C';
                    break;
                case P.COU:
                    c = b + 'U';
                    break;
                case P.FOR:
                    c = b + 'F';
                    break;
                case P.MUS:
                    c = b + 'R';
                    break;
                case P.PAW:
                    c = b + 'P';
                    break;
                case P.EMP:
                    c = ' ';
                    break;
                default:
                    c = '/';
                    break;
            }
            return (char)c;
        }
    }

